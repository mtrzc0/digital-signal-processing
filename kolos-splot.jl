)
    N=length(x)
    M=length(h)
    K=N+M+1
    y=zeros(Float64, K)
    for n in 1:K
        for m in 1:M
            if n-m > 0 && n-m+1<=N
                y[n] = y[n] + h[m]*x[n-m+1]
            end
        end
    end
    return sum(y .* y)
end
